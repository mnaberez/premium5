from premium5.digital import LogicInput, LogicOutput, Level


class UPD16432B(object):
    """NEC uPD16432B LCD controller.

    Receives SPI data at the line level: STB for framing, CLK for
    bit timing, DAT for data.  Shifts in bits on rising CLK edges
    and processes complete bytes through the command state machine.

    For key scan, shifts out response bits on DAT_OUT on falling
    CLK edges.
    """

    def __init__(self):
        # electrical interface
        self.stb_in = LogicInput(pull_level=Level.LOW)
        self.clk_in = LogicInput(pull_level=Level.HIGH)
        self.dat_in = LogicInput(pull_level=Level.LOW)
        self.dat_out = LogicOutput()

        self.stb_in.on_rising  = self._on_stb_rising
        self.stb_in.on_falling = self._on_stb_falling
        self.clk_in.on_rising  = self._on_clk_rising
        self.clk_in.on_falling = self._on_clk_falling

        self._init_buffers()
        self._init_command_state()
        self._init_shift_state()

    # init

    def _init_buffers(self):
        """Public access state buffers"""
        self.display_ram = bytearray(0x19)
        self.pictograph_ram = bytearray(0x08)
        self.chargen_ram = bytearray(7 * 0x10)
        self.led_ram = bytearray(1)
        self.key_data = bytearray(4)
        self.display_pixels = bytearray(7 * 0x19)

    def _init_command_state(self):
        """Internal state maintained between commands"""
        self._current_ram = None
        self._address = 0
        self._increment = False
        self._key_index = 0

    def _init_shift_state(self):
        """Shift register state (lasts for one command only)"""
        self._shift_in  = 0x00  # 8-bit shift register for input
        self._shift_out = 0x00  # 8-bit shift register for output
        self._shift_count = 0   # number of bits shifted
        self._on_byte_func = self._on_byte_command

    # callbacks: electrical

    def _on_stb_rising(self):
        """Rising edge of STB: chip is now enabled; prepare for new command"""
        self._init_shift_state()

    def _on_stb_falling(self):
        """Falling edge of STB: command completed; render display pixels"""
        self._render_display_pixels()

    def _on_clk_rising(self):
        """Rising edge of CLK: shift in a bit if enabled"""
        if self.stb_in.high:
            self._shift_in = (self._shift_in << 1) | int(self.dat_in)
            self._shift_count += 1
            if self._shift_count == 8:
                self._on_byte_func(self._shift_in & 0xFF)
                self._shift_in = 0x00
                self._shift_count = 0

    def _on_clk_falling(self):
        """Falling edge of CLK: shift out a bit if enabled"""
        if self.stb_in.high:
            if (self._shift_out >> (7 - self._shift_count)) & 1:
                self.dat_out.set_high()
            else:
                self.dat_out.set_low()

    # callbacks: our internal ones called after a byte is received

    def _on_byte_command(self, rx_byte):
        if (rx_byte & 0xC7) == 0x44:  # key data request
            self._key_index = 0
            self._shift_out = self.key_data[self._key_index]
            self._on_byte_func = self._on_byte_keys
        else:
            cmd_type = rx_byte & 0xC0
            if cmd_type == 0x80:
                self._process_address_setting_cmd(rx_byte)
                self._on_byte_func = self._on_byte_data
            elif cmd_type == 0x40:
                self._process_data_setting_cmd(rx_byte)
                self._on_byte_func = self._on_byte_data
            else:
                self._on_byte_func = self._on_byte_unknown

    def _on_byte_data(self, rx_byte):
        if self._current_ram is not None:
            self._write_data_byte(rx_byte)

    def _on_byte_keys(self, rx_byte):
        self._key_index += 1
        self._key_index %= len(self.key_data)
        self._shift_out = self.key_data[self._key_index]

    def _on_byte_unknown(self, rx_byte):
        pass

    # internal helpers

    def _process_address_setting_cmd(self, cmd):
        address = cmd & 0x1F
        if self._current_ram is self.chargen_ram:
            if address < 0x10:
                self._address = address * 7
            else:
                self._address = 0
        elif self._current_ram is None:
            self._address = 0
        else:
            self._address = address
            self._wrap_address()

    def _process_data_setting_cmd(self, cmd):
        mode = cmd & 0x07
        ram_table = {
            0: self.display_ram,
            1: self.pictograph_ram,
            2: self.chargen_ram,
            3: self.led_ram,
        }
        if mode in ram_table:
            self._current_ram = ram_table[mode]
        else:
            self._current_ram = None
            self._address = 0
        if mode in (0, 1):
            self._increment = not bool(cmd & 0x08)
        else:
            self._increment = True
            self._address = 0

    def _write_data_byte(self, b):
        if self._address < len(self._current_ram):
            self._current_ram[self._address] = b
        if self._increment:
            self._address += 1
            self._wrap_address()

    def _wrap_address(self):
        if self._address >= len(self._current_ram):
            self._address = 0

    def _render_display_pixels(self):
        for i in range(len(self.display_ram)):
            char_code = self.display_ram[i]
            if char_code < 0x10:
                src = self.chargen_ram
                offset = char_code * 7
            else:
                src = CHARSET_VW_PREMIUM_5
                offset = char_code * 7
            self.display_pixels[i * 7:(i + 1) * 7] = src[offset:offset + 7]


