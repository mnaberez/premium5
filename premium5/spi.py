def _encode_charset(text):
    data = [0] * 7 * 256
    pixels = [ c for c in text if c in ('.', '#') ]
    pixel_index = 0
    for group in range(32):
        for line in range(7):
            for char in range(8):
                data_offset = (group * 8 * 7) + (char * 7) + line
                data[data_offset] |= line << 5
                for bit in range(4, -1, -1):
                    if pixels[pixel_index] == '#':
                        data[data_offset] |= 2 ** bit
                    pixel_index += 1
    return tuple(data)

CHARSET_VW_PREMIUM_5 = _encode_charset(_CHARSET_VW_PREMIUM_5)


